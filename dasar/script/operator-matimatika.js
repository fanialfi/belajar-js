// aritmatik
/*
 * + => pertambahan
 * - => pengurangan
 * * => perkalian
 * ** => eksponen
 * / => pembagian
 * % => sisa bagi
 */
console.log(1 + 2); // 3
console.log(3 - 4); // -1
console.log(1 + "1"); // 11
console.log(1 - "2"); // -1
console.log(1 * 1); // 1
console.log(1 * "1"); // 1
console.log(100 ** 5);
console.log(10 / "10"); // 1
console.log(10 % 3); // 1
console.log(100 - "Hai"); // NaN
